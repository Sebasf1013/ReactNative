// Declarar la variable altura y asignar el valor 10
// Declarar la variable base y asignar el valor 4
// Mostrar en consola el cálculo del perímetro de un rectangulo (suma de los lados) y el área (base por altura).

const altura = 10; 
const base = 4; 
const perimetro = altura + base; 
const area = base * altura; 

console.log(`El perimetro del rectangulo de altura ${altura} y base ${base} es: ${perimetro}. El area es ${area}`);
